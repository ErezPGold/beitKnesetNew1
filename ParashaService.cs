        public string GetWeeklyParasha(DateTime today)
        {
            // מצא את השבת הקרובה (אם היום שבת — היום עצמו)
            int daysToShabbat = ((int)DayOfWeek.Saturday - (int)today.DayOfWeek + 7) % 7;
            DateTime shabbat = today.Date.AddDays(daysToShabbat);
            return Schedule.TryGetValue(shabbat, out var p) ? p : "—";
        }
